int ang, oang, range, orange, d, dir, posx, posy,
x, y, dist, flag, attack, timer, clock, deg1, asin, acos;

fastradar() /* Count enemies */
{
  return (
   (scan(deg1,    10)!=0)
  +(scan(deg1+20, 10)!=0)
  +(scan(deg1+40, 10)!=0)
  +(scan(deg1+60, 10)!=0)
  +(scan(deg1+80, 10)!=0)
  +(scan(deg1+100,10)!=0)
  );
}

stop()
{
  fire2(dir,0);
  while( speed() > 59 );
}

degree(xx,yy) /* Direction to x,y */
int xx,yy;
{
    return(dir=(360+((xx-=loc_x())<0)*180+atan(((yy-loc_y())*100000)/xx)));
}

dista(dx,dy) /* Square distance (avoid sqrt()) */
int dx,dy;
{
    return (((dx-=loc_x())*dx+(dy-=loc_y())*dy));
}

corner() /* Get closer to corner */
{
    drive(degree(x, y), 100);
    while( (dist = dista(x, y)) > 8100 )
    {
        if(dist < 25600) ;
        else fire(dir);
    }
    stop();
    if ( ++timer%12 == 1 )
    {
        if ( fastradar() < 2 ) {
            while(1)
            {
                if (((posx=loc_x())%880)<120) dir=180*(posx>500);
                else if (((posy=loc_y())%880)<120) dir=90+180*(posy>500);
                else if (range>610) dir=ang+25;
                else if (range<180) dir=ang+195;
                else dir=ang+180*(clock^=1);

                fire2(dir,100);
                fire2(dir,100);
                fire2(dir,100);
            }
        }
        if( damage() < 60 ) attack|=2; else attack=0;
    }
}

int radar3(sc)
{
       if ( scan(sc,     10) ) ;
  else if ( scan(sc+=20, 10) ) ;
  else return 0;
  ang = sc;
  return 1;
}

fire(dir)
{

    if (speed()<100) drive(dir,100); else { if (scan(dir,10)) ang=dir; if (range>850) { ang+=120; } }

    if (scan(ang,10)) {
        asin=(sin(ang-dir)/14384);
        acos=(cos(ang-dir)/3796)-230;

        find();
        if (orange=scan(oang=ang,3)) {
            find();
            cannon(ang+(ang-oang)*((880+(range=scan(ang,10)))/482)-asin,
            range*230/(orange-range-acos));
        }  else search();
    } else search();
}

find()
{
  if(scan(ang-13,10)) ang-=5;
  else if(scan(ang+13,10)) ang+=5;
  if(scan(ang+12,10)) ang+=4;
  else if(scan(ang-12,10)) ang-=4;
  if(scan(ang-11,10)) ang-=2;
  if(scan(ang+11,10)) ang+=2;
}

search()
{
  if (range=scan(ang+=350,10)) return cannon(ang,range);
  if (range=scan(ang+=20,10))  return cannon(ang,range);
  if (range=scan(ang+=320,10)) return cannon(ang,range);
  if (range=scan(ang+=60,10))  return cannon(ang,range);
  if (range=scan(ang+=280,10)) return cannon(ang,range);

  search(ang-=220);
}

fire2(dir,v)
{
    drive(dir,v);
    if (range=scan(oang=ang,10))
    {
        if (scan(ang-8,5))
        {
            if (scan(ang-=5,2)) ;
            else ang-=4;
        }
        else
        {
            if (scan(ang+8,5))
            {
                if (scan(ang+=5,2)) ;
                else ang+=4;
            }
            else
            {
                if (scan(ang,1)) ;
                else if (scan(ang-=3,2)) ; else ang+=6;
            }
        }
        return(cannon((ang<<1)-oang,(scan(ang,10)<<1)-range));
    }
    else
    {
        if(range=scan(ang+=20,10)) cannon(ang,range);
        else if(range=scan(ang-=40,10)) cannon(ang,range);
        else ang+=80;
    }
}

lightsaber(delta)
{
  d=damage()+delta;
  while(dista(x,y) < (123450-4925*(damage()>d))) fire(dir);
}

main()
{
    degree(x=20+960*(posx=loc_x()>499), y=20+960*(posy=loc_y()>499));
    corner(deg1=90*((posy<<1)|(posx^posy)));
    while (1)
    {
        if (radar3(deg1+70*flag))
        {
            dir=deg1 + (30<<flag);

            if (attack & (1<<flag))
            {
                if (damage(lightsaber(5))>d) attack&=~(1<<flag);/* else attack|=(1<<flag);*/
            }
            else
            {
                int t;
                d=damage();
                while((speed()<100) || ((++t<2) && (damage()==d))) fire(dir);
                if(damage()>d)
                {
                    lightsaber(0);
                }
            }
        }
        else
        {
            dir=deg1 + 15 + (60*flag);
            lightsaber(5);
        }
        corner(stop(flag^=1));
    }
}
