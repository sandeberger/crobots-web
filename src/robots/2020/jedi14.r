int
    ang,oang,range,orange,d,dir,posx,posy,flag,timer,
    enemies,t,b,deg1,deg2,asin,acos,deg,brange;

stop()
{
  fire2(drive(dir,t=0));
  while(speed()>59) ;
}

fastradar() /* Count enemies */
{
  return enemies=3
  -(scan(deg1+100,10)!=0)
  -(scan(deg1+80, 10)!=0)
  -(scan(deg1+60, 10)!=0)
  -(scan(deg1+40, 10)!=0)
  -(scan(deg1+20, 10)!=0)
  -(scan(deg1,    10)!=0)
  ;
}

runX()
{
  dir=180*!posx;
  if(posx) { while(loc_x()<900) fire3(dir,100); while(loc_x()<950) drive(dir,100); }
  else     { while(loc_x()>100) fire3(dir,100); while(loc_x()> 50) drive(dir,100); }
  stop();
}

runY()
{
  dir=270-180*posy;
  if(posy) { while(loc_y()<900) fire3(dir,100); while(loc_y()<950) drive(dir,100); }
  else     { while(loc_y()>100) fire3(dir,100); while(loc_y()> 50) drive(dir,100); }
  stop();
}

corner()
{
    if (speed()) stop(); if (flag^posx^posy) runY(runX()); else runX(runY());
}

xmax() { while(loc_x()>86) fire3(dir,100);   while(loc_x()>50) drive(dir,100);   stop(); }
xmin() { while(loc_x()<914) fire3(dir,100);  while(loc_x()<950) drive(dir,100);  stop(); }
ymax() { while(loc_y()>86) fire3(dir,100);   while(loc_y()>50) drive(dir,100);   stop(); }
ymin() { while(loc_y()<914) fire3(dir,100);  while(loc_y()<950) drive(dir,100);  stop(); }

move() {
    if (range>710) {
        ang=deg1;
    }
    fire3(dir,100);
    if (damage()<60) fire();
    fire3(dir,100);
    fire3(dir+=180,0);
}

fire()
{
  if (speed()<100) drive(dir,100);

  if (scan(ang,10)>100)
  {
      asin=(sin(ang-dir)/14384);
      acos=(cos(ang-dir)/3796)-230;

      ang-=18*(scan(ang-18,10)>0);
      ang+=18*(scan(ang+18,10)>0);

      if(scan(ang-16,10)) ang-=8;
      else if(scan(ang+16,10)) ang+=8;
      if(scan(ang-12,10)) ang-=4;
      else if(scan(ang+12,10)) ang+=4;
      if(scan(ang-11,10)) ang-=2;
      if(scan(ang+11,10)) ang+=2;

      if (orange=scan(oang=ang,3))
        {
            if(scan(ang-13,10)) ang-=5;
            else if(scan(ang+13,10)) ang+=5;
            if(scan(ang+12,10)) ang+=4;
            else if(scan(ang-12,10)) ang-=4;
            if(scan(ang-11,10)) ang-=2;
            if(scan(ang+11,10)) ang+=2;

            cannon(ang+(ang-oang)*((880+(range=scan(ang,10)))/482)-asin,
                   range*230/(orange-range-acos));
        }  else search();
  } else search();
}

search()
{
  if (scan(ang-=350,10)) return fire2();
  if (scan(ang-=20,10))  return fire2();
  if (scan(ang-=320,10)) return fire2();
  if (scan(ang-=60,10))  return fire2();
  if (scan(ang-=280,10)) return fire2();
  if (scan(ang-=100,10)) return fire2();
  if (scan(ang-=240,10)) return fire2();
  if (scan(ang-=140,10)) return fire2();
  if (scan(ang-=200,10)) return fire2();
  if (scan(ang-=180,10)) return fire2();
  if (scan(ang-=160,10)) return fire2();
  if (scan(ang-=220,10)) return fire2();
  if (scan(ang-=120,10)) return fire2();
  if (scan(ang-=260,10)) return fire2();
  if (scan(ang-=80,10))  return fire2();
  if (scan(ang-=300,10)) return fire2();
  if (scan(ang-=40,10))  return fire2();
  if (scan(ang-=340,10)) return fire2();
}

fire2()
{
    if (range=scan(oang=ang,10))
    {
        if (scan(ang-8,5))
        {
            if (scan(ang-=5,2)) ;
            else ang-=4;
        }
        else
        {
            if (scan(ang+8,5))
            {
                if (scan(ang+=5,2)) ;
                else ang+=4;
            }
            else
            {
                if (scan(ang,1)) ;
                else if (scan(ang-=3,2)) ; else ang+=6;
            }
        }
        return(cannon((ang<<1)-oang,(scan(ang,10)<<1)-range));
    }
    else search();
}

fire3(dir,v)
{
    drive(dir,v);
    if (range=scan(oang=ang,10))
    {
        if (scan(ang-8,5))
        {
            if (scan(ang-=5,2)) ;
            else ang-=4;
        }
        else
        {
            if (scan(ang+8,5))
            {
                if (scan(ang+=5,2)) ;
                else ang+=4;
            }
            else
            {
                if (scan(ang,1)) ;
                else if (scan(ang-=3,2)) ; else ang+=6;
            }
        }
        return(cannon((ang<<1)-oang,(scan(ang,10)<<1)-range));
    }
    else
    {
        if(range=scan(ang+=20,10)) cannon(ang,range);
        else if(range=scan(ang-=40,10)) cannon(ang,range);
        else ang+=80;
    }
}

int look(a)
{
       if (range=scan(a,10))    ;
  else if (range=scan(a+=20,10));
  else return 0;
  ang=a;
  return range>820;
}

slowradar(skip)
{
  if (skip) return;
  deg=deg1-20; brange=1500; enemies=3;
  while( (deg<=deg2) && enemies)
  if (range=scan(deg+=20, 10))
  {
      --enemies;
      if (range<brange)
      {
          ang=deg;
          brange=range;
      }
  }
}


main()
int heading,xy,safe,skip;
{
    posx=loc_x(posy=(loc_y()>499))>499;
    skip=( fastradar(corner(deg2=(deg1=(90*(heading=(posy<<1)|(posx^posy))))+100)) < 1);
    while (1) {
      if (enemies>1) {
        drive(dir=deg1+15,100);
        while(1) /* F2F */
        {
            if (((posx=loc_x())%880)<120) dir=180*(posx>500);
            else if (((posy=loc_y())%880)<120) dir=90+180*(posy>500);
            else if (range>600) dir=ang+25+(b^=1)*310;
            else if (range<150) dir=ang+165+(b^=1)*30;
            else dir=ang+180*(b^=1);

            fire3(dir,100);
            fire3(dir,100);
            fire3(dir,100);
        }
      }
      while(enemies==1) /* 3vs3 */
      {
          if (look(deg1))
          {
              dir=deg1+(30<<flag);
              if (look(deg1+(70*flag)))
              {
                  fire();
                  while(scan(ang,10)>680) fire();
                  corner();
                  flag^=1;
              }
              else if (scan(dir,10))
              {
                  ang=dir;
                  fire();
                  while(scan(ang,10)>640) fire();
                  corner();
                  flag^=1;
              }
              else
              {
                  while(++t<4) fire();
                  corner();
                  if (damage()>d)
                  {
                      flag^=1;
                  }
                  d=damage()+4;
              }
              if ((++timer%2)&&(range>400))
              {
                  if ((timer>64)&&(damage()<40)) enemies=2;
                  else slowradar(skip);
              }
          }
          else
          {
              dir=deg1;
              while(t<4)
              {
                  if (heading&1) xy=loc_y();  else xy=loc_x();
                  if (heading&2) safe=xy>180; else safe=xy<820;

                  if (safe) fire();
                  else
                  {
                      fire3(dir+=90,0); if (scan(dir,10)) ang=dir; ++heading; timer+=++t;
                  }
              }
              fastradar(t=0);
          }
      }
      while (enemies<1) /* 4vs4 */
      {
          if (scan(deg1,10)||scan(deg1+90,10)) {
              if (posy) {
                  ymin(move(dir=300-60*(posx)));
              }
              else {
                  ymax(move(dir=60+60*(posx)));
              }

              if (posx) {
                  xmin(move(dir=150+60*(posy)));
              }
              else {
                  xmax(move(dir=30-60*(posy)));
              }
          } else {
              if (posy) {
                  ymin(move(dir=270));
              } else {
                  ymax(move(dir=90));
              }

              if (posx) {
                  xmin(move(dir=180));
              }
              else {
                  xmax(move(dir=0));
              }
          }

          if ((++timer%4 == 1)&&(range>400))
          {
              if ((timer>32)&&(damage()<40)) enemies=1;
              else slowradar(0);
          }
      }
    }
}

