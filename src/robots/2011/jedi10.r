Jedi9 è stato un fallimento totale. Jedi10 nasce dalle ceneri di Jedi9 e
promette di fare altrettanto schifo.
Jedi10 abbandona il movimento a "V" che ha caratterizzato le mie creature per
diversi anni in favore dell'evergreen triangolo.
Specifiche:
- Il lato di oscillazione è quello in cui si subiscono meno danni;
- L'oscillazione è limitata in base alla distanza del nemico e ai danni;
- Se l'angolo opposto è libero l'ampiezza è costante.
Specifiche 3vs3 :
- Come il 4vs4 ma aumenta l'ampiezza di oscillazione; se l'angolo opposto è
occupato, il lato di oscillazione viene cambiato subito.
Specifiche f2f:
- Spudoratamente copiata da Proton. Spero sia la scelta tattica giusta.
Note tecniche:
- Il calcolo dei danni subiti ha un bug : se lo correggo l'efficienza peggiora.
- Test statistici sono stati effettuati per incrementare le aspettative e,
ovviamente, ridurre l'efficienza reale.
Ringraziamenti:
A tutti gli autori a cui ho vergognosamente sottratto il codice che
inutilmente ho tentato di migliorare.
