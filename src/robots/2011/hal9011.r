int
	ang,
	oang,
	range,
	orange,   /* Variabili di puntamento                              */
	d,dmax,   /* Variabili temporanee salva danni subiti              */
	dir,      /* Direzione del cammino                                */
	posx,     /* Variabili ad 1 bit                                   */
	posy,     /* salva posizione                                      */
	dist,     /* Distanza dal corner                                  */
	flag,     /* Bit-oriented flag per la gestione del movimento      */
	timer,
	fp,att,
	enemies,  /* Conta nemici superstiti                              */
  t,        /* Timer oscillazione                                   */
  td, tl,   /* Variabili di utilità                                 */
  deg1,     /* Limite inferiore di scansione per contare i nemici   */
  deg2,     /* Limite superiore di scansione per contare i nemici   */
  dscan, dvector /* Array bidimensionali                      */
  ;

stop()
{
  spara(drive(dir,t=0));  
  while(speed()>59) ;
}

int radar()
{
int deg=deg1-20;
  enemies=3;
  while((deg<=deg2)&& enemies) enemies-=(scan(deg+=20, 10)!=0);
  if (enemies>1)
  {
    int b;
    att=fp=5;
    if (scan(deg1+45,10)) dir=ang=deg1+45;
    else dir=deg1+15;
    while(1)
    {
      if(t%2) {
              if (((posx=loc_x())%880)<120) dir=180*(posx>500);
              else if (((posy=loc_y())%880)<120) dir=90+180*(posy>500);
              else {
                      if(orange<210) dir=(ang/90)*90;
                      else if (orange<680) dir=ang+80+(b^=1)*190; 
                      else dir=ang+25+(b^=1)*235;
              }
      }
      spara(drive(dir,100));
    }
  }
}

fire()
{
  if (range=scan(oang=ang,10))  
  {    
		if (scan(ang+350,10)) ang-=1; else ang+=1;
    		if (scan(ang+10,10)) ang+=1; else ang-=1; 
		      cannon(ang,(scan(ang,10)<<1)-range);
 	} 
	else 
	{
      	if (range=scan(ang+=340,10)) return cannon(ang,range); 
	      if (range=scan(ang+=40,10))  return cannon(ang,range);  
	      while (!(range=scan(ang+=20,10))) ; 
      	cannon(ang,range);
  }
}

spara()
{ 
        ++t; 
        if ((orange=scan(ang, 10)) ) { 
                if (scan(ang-9,4)) { 
                        if (scan(ang-=13,4)) { 
                                if(scan(ang-3,fp)) ang-=fp;
                                else ++ang;
                        }  else if (scan(ang-fp,fp)) ang-=fp;
                } else if(scan(ang+9,fp)) { 
                        if (scan(ang+=13,fp)) ang+=fp;
                        else --ang;
                }  else if(scan(ang+4,fp)) ang+=fp;
                else --ang;

        }  else if ((orange=scan(ang-=20,10))) { 
                if (scan(ang-9,10)) { 
                        if (scan(ang-=13,fp)) ang-=fp;
                        else ++ang;
                } else if(scan(ang+9,10)) ang+=6; 
        }  else if ((orange=scan(ang+=40,10))) { 
                if (scan(ang+9,10)) ang+=9;
        }  else if (!(orange=scan(ang+=20,10))) { 
                if ((orange=scan(ang+=21,10))) { 
                        if (orange>900) { 
                                if(!att) ang+=41; 
                                return;
                        } 
                } else { 
                        if (!(scan(ang+=21,10))) ang+=40; 
                        return; 
                } 
	} 
        if (range=scan(ang,10)){  
                cannon (ang, range*165/(165+orange-range) ); 
                if(att); else if(range>720) if(range>orange || range>900) {
                                return ang+=41;
                        }

        }  
} 

runX()
{
  dir=180*!posx;
  if(posx) { while(loc_x()<905) fire(drive(dir,100)); while(loc_x()<950) drive(dir,100); }
  else     { while(loc_x()> 95) fire(drive(dir,100)); while(loc_x()> 50) drive(dir,100); }
  stop();
}

runY()
{
  dir=270-180*posy;
  if(posy) { while(loc_y()<905) fire(drive(dir,100)); while(loc_y()<950) drive(dir,100); }
  else     { while(loc_y()> 95) fire(drive(dir,100)); while(loc_y()> 50) drive(dir,100); }  
  stop();
}

corner()
{
  if (speed()) stop();
  if ((flag>>4)^posx^posy) runY(runX()); else runX(runY());
}

main() /* Inizializza alcune variabili ed innesca la routine principale */
int dperf;
{
  corner(posx=loc_x(posy=(loc_y(tl=td=32))>499)>499,fp=3);
  radar(deg2=(dvector=(dscan=deg1=(90*((posy<<1)+(posx^posy))))+30)+70);
  dvector|=((dvector+30)<<16);
  dscan|=((deg1+70)<<16);
  while (1)
  {
    dperf=((dscan>>flag)&65535)+1;
    drive(dir=(dvector>>flag)&65535,100);
    if (scan(dperf,10))
    {
      ang=dperf;
      while(scan(ang, 10)>(600+damage())) fire();
      corner();
      if ((d=damage()-d)>dmax)
      {
          dmax=d; flag^=16;
      }
    }
    else
    {
      while(t<14) spara();
      corner(tl=timer);
    }
    if (++timer>tl)
  	{
  	    radar(tl=timer+(td>>=1)+1);
    }
  }
}
