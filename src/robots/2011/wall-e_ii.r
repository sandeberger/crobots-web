int ang,        /* Angolo di scanning                                   */
    oang,       /* Angolo di scanning precedente                        */
    range,      /* Gittata                                              */
    dir,        /* Direzione del cammino                                */
    posx,
    posy,       /* Variabili temporanee ad un bit salva posizione       */
    dvector,    /* Array bidimensionale                                 */
    x,y,        /* Coordinate del corner                                */
    flag,       /* Flag di stato                                        */
    m,          /* Flag correggi ampiezza sparo                         */
    timer,      /* Timer e numero nemici                                */
    deg,        /* Inizio scansione conta nemici                        */
    i;          /* Variabile temporanea                                 */

fire(d)
{
	drive(d,100);
	if ((range=scan(oang=ang,10))&&(range<850)) {
		if (scan(ang-8,5))  { if (scan(ang-=5,2)) ; else ang-=4; return(cannon(ang+(ang-oang),2*scan(ang,10)-range)); }
		if (scan(ang+8,5))  { if (scan(ang+=5,2)) ; else ang+=4; return(cannon(ang+(ang-oang),2*scan(ang,10)-range)); }
		if (scan(ang,10))   { if (scan(ang-=2,2)) ; else ang+=4; return(cannon(ang+m*(ang-oang),2*scan(ang,10)-range)); }
	} 
	else if(scan(ang+=20,10));
	else if(scan(ang-=40,10));
	else if(scan(dir,10)) ang=dir;
	else ang+=80;
}

degree(x,y)
int x,y;
{
   return(dir=(360+((x-=loc_x())<0)*180+atan(((y-loc_y())*100000)/x)));
}

dista(dx,dy) /* Distanza al quadrato (evita una sqrt())   */
int dx,dy;
{
  return (((dx-=loc_x())*dx+(dy-=loc_y())*dy));
}

corner()
{
  degree(x,y);
	while(dista(x,y) > 6400) fire(dir);
	drive(dir, i=0);
}

main() /* Inizializza alcune variabili ed innesca la routine principale */
{
 int b;
 corner(y=20+960*(posy=loc_y(x=20+960*(posx=loc_x()>499))>499));
 dvector=(deg=(90*((posy<<1)+(posx^posy))))+30;
 dvector|=((dvector+30)<<16);
 while (1)
 {
        while((timer<2) && (i < 140)) { timer+=(scan(deg+i,10)>0); i+=20; }
        if (timer<2)
        {
    	   m=1;
    	   while(1)
         { 	/*attacco del f2f*/
		
		            if (((posx=loc_x())%880)<120) dir=180*(posx>500);
                else if (((posy=loc_y())%880)<120) dir=90+180*(posy>500);
            		else if (range>600) dir=ang+25;
            		else if (range<150) dir=ang+195;
            		else dir=ang+180*(b^=1);
            				
            		fire(dir);
            		fire(dir);
            		fire(dir);
         }
        }
        else
          timer=8<<(damage(dir=(dvector>>flag)&65535)<40);
     		while(--timer) fire(dir);
     		corner(drive(dir, flag^=16));
 }
}
