  sk,       /* Variabile misteriosa di Mick                         */
  fp,       /* Variabile misteriosa di Mick                         */
  i, td, tl,/* Variabili di utilità                                 */
  deg,      /* Limite superiore di scansione per contare i nemici   */
  up, low,  /* Limiti di oscillazione nell'F2F                      */
  clock,    /* Switch oscillazione nell'F2F                         */
