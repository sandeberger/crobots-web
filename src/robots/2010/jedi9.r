Jedi9 è un'evoluzione meno aggressiva di Jedi8. Mantiene lo stesso movimento
oscillatorio nell'angolo ma per gran parte del match ne riduce l'ampiezza
(fase I).
Jedi9 utilizza lo stesso movimento di Jedi8 (funzione lightsaber()) se :
- colpito; si suppone che l'avversario sia - per qualche motivo - più vicino e
quindi lo attacca
- sono passati un certo numero di cicli (fase II)
Durante il movimento più aggressivo registra eventuali danni subiti e si
riconfigura per utilizzare nuovamente il movimento difensivo.
Se l'angolo opposto è libero, oscilla a 15° dal lato con un range più ampio,
invece che a 30°.

Il controllo della tipologia di match F2F viene fatta prima di raggiungere
l'angolo. Nel 4vs4 il conteggio degli avversari è invece periodico, con maggiore
frequenza al passare dei cicli macchina.

L'attacco finale è a forma di quadrato che cambia ampiezza se si subiscono danni
superiori all'11% durante una singola rotazione.

Test statistici sono stati utilizzati per calibrare al meglio alcune costanti
del codice.

Novità:
Per la prima volta (?) vengono utilizzati gli array. In dettaglio, vengono
utilizzati array bidimensionali sfruttando i 16 bit meno significativi della
variabile per un indice e i 16 bit più significativi per l'altro. Allo scopo si
usano shift di 16 bit e operazioni di and / or bit a bit con maschere di bit per
accedere in lettura / scrittura ai due elementi dell'array.
Gli array vengono utilizzati per salvare separatamente le informazioni dell'
oscillazione e scansione (angolo, flag attacco / difesa, ...) per ciascuno dei
due lati a dx e sx dell'angolo dove il robot oscilla.
Alla fine non c'è un miglioramento dell'efficienza... Mi andava di usarli e
basta :-)
Spero in ogni caso di aver dato uno spunto per sviluppi futuri...
*/

