fire(dir) /* Many thanks to Daniele Nuzzo */
{
  int asin,acos;
  /*if (safe) return;*/
  if (speed()<100) drive(dir,100); 
      
  if (scan(ang,10)>50)
  {  
      asin=(sin(ang-dir)/14384);
      acos=(cos(ang-dir)/3796)-230;

      ang-=18*(scan(ang-18,10)>0);  
      ang+=18*(scan(ang+18,10)>0); 

      if(scan(ang-16,10)) ang-=8;
      else if(scan(ang+16,10)) ang+=8;
      if(scan(ang-12,10)) ang-=4;
      else if(scan(ang+12,10)) ang+=4;
      if(scan(ang-11,10)) ang-=2;
      if(scan(ang+11,10)) ang+=2;

      if (orange=scan(oang=ang,3))
        {
            if(scan(ang-13,10)) ang-=5;
            else if(scan(ang+13,10)) ang+=5; 
            if(scan(ang+12,10)) ang+=4;
            else if(scan(ang-12,10)) ang-=4;
            if(scan(ang-11,10)) ang-=2;
            if(scan(ang+11,10)) ang+=2;

            cannon(ang+(ang-oang)*((880+(range=scan(ang,10)))/482)-asin,
                   range*230/(orange-range-acos)); 
        }  else search(); 
  } else search();  
}

search()
{
  if (scan(ang-=350,10)) return fire2();
  if (scan(ang-=20,10))  return fire2();
  if (scan(ang-=320,10)) return fire2();
  if (scan(ang-=60,10))  return fire2();
  if (scan(ang-=280,10)) return fire2();
  if (scan(ang-=100,10)) return fire2();
  if (scan(ang-=240,10)) return fire2();
  if (scan(ang-=140,10)) return fire2();
  if (scan(ang-=200,10)) return fire2();
  if (scan(ang-=180,10)) return fire2();
  if (scan(ang-=160,10)) return fire2();
  if (scan(ang-=220,10)) return fire2();
  if (scan(ang-=120,10)) return fire2();
  if (scan(ang-=260,10)) return fire2();
  if (scan(ang-=80,10))  return fire2();
  if (scan(ang-=300,10)) return fire2();
  if (scan(ang-=40,10))  return fire2();
  if (scan(ang-=340,10)) return fire2();
}

fire2()
{

  if (range=scan(oang=ang,10)) {
    if (range>500) return cannon(ang,range);
    if (scan(ang+350,10)) ang+=355; else ang+=5;
    if (scan(ang+350,10)) ang+=357; else ang+=3; 
    cannon((ang<<1)-oang,(scan(ang,10)<<1)-range);
  } else search();   
}

stop()
{
  drive(dir,0);  
  while(speed()>50) if (orange=scan(ang,10)) {
      if (range=scan((ang=(6*(scan((ang+=(10*(scan((oang=ang)+10,10)>0)-5))+10,10)>0)-3)+ang),10))
        cannon(ang+(ang-oang),range+((range-orange)<<1));
    }
  /*drive(dir,100);*/
}

degree(xx,yy) /* Direzione per x,y */
int xx,yy;
{
   return(dir=(360+((xx-=loc_x())<0)*180+atan(((yy-loc_y())*100000)/xx)));
}

dista(dx,dy) /* Distanza al quadrato (evita una sqrt())   */
int dx,dy;
{
        return (((dx-=loc_x())*dx+(dy-=loc_y())*dy));
}

corner() /* Si avvicina al corner */ 
{
	drive(degree(x,y), 100);
	while((dist=dista(x,y)) > 6325)
  {
   if(dist<10450) ;
   else spara();  
  }
  stop(t=0); sk=1;
}

int radar(sc)
{
int sc, en, dx;
  while((en<2) && (dx<160)) {
   en+=radar2(sc+dx); dx+=20;
  }
  return en;
}

int radar2(sc)
int sc;
{
  return (
   (scan(sc+10, 10)!=0) + (scan(-sc-10, 10)!=0)
  );
}

int radar3(sc)
int sc;
{
  return (
   scan(sc, 10) || scan(sc+20, 10)
  );
}

spara() /* Many thanks to Mick Messina */
{
        ++t;
        if ((orange=scan(ang, 10)) ) { 
                if (scan(ang-9,4)) { 
                        if (scan(ang-=13,4)) { 
                                if(scan(ang-3,fp)) ang-=fp;
                                else ++ang;
                        }  else if (scan(ang-fp,fp)) ang-=fp;
                } else if(scan(ang+9,fp)) { 
                        if (scan(ang+=13,fp)) ang+=fp;
                        else --ang;
                }  else if(scan(ang+4,fp)) ang+=fp;
                else --ang;

        }  else if ((orange=scan(ang-=20,10))) { 
                if (scan(ang-9,10)) { 
                        if (scan(ang-=13,fp)) ang-=fp;
                        else ++ang;
                } else if(scan(ang+9,10)) ang+=6; 
        }  else if ((orange=scan(ang+=40,10))) { 
                if (scan(ang+9,10)) ang+=12;
        }  else if ((orange=scan(ang+=20,10)));
	else { 
                if(sk) {
			if ((orange=scan(ang-=80,10))) return cannon(ang,orange);
			else if ((orange=scan(ang-=20,10))) return cannon(ang,orange);
			else if ((orange=scan(ang+=120,10))) return cannon(ang,orange);
			else if ((orange=scan(ang+=20,10))) return cannon(ang,orange);
			else if ((orange=scan(ang-=160,10))) return cannon(ang,orange);
			else return ang+=260;
		}
                if ((orange=scan(dir,10))) {
                        if (orange>850) {
                                return ang+=40;
                        } else ang=dir;
                } else { 
                        if ((orange=scan(ang+=20,10))) return cannon(ang,orange); 
                        return ang+=40; 
                } 
	} 
        if (range=scan(ang,10)){  
                cannon (ang, range*145/(145+orange-range) ); 
                if(range>740) if(!sk) if(range>orange || range>900) {
                                return ang+=40;
                        }
        }  else if(scan(ang-20,10)) ang-=20; 
        else if(scan(ang+=20,10));
	else ang+=40; 
} 

setup() {
/*
* Setup main variables
*/
  degree(x, y);
  corner(fp=3);
  dvector=(dang=(deg=dscan=deg1=(90*((posy<<1)+(posx^posy))))+15)+15;
  dvector|=((dvector+30)<<16);
  dang|=((dang+60)<<16);
  dscan|=((deg1+70)<<16);
  deg+=320;
  tl=td=38;
  /*attack=65537;*/
}

f2f()
{
  up=725;
  low=275;
  while(1)
  {
    d=damage();
    while(loc_x() < up)  fire(0);   stop();
    while(loc_y() > low) fire(270); stop();
    while(loc_x() > low) fire(180); stop();
    while(loc_y() < up)  fire(90);  stop();
    				
    if ((damage()-d)>11) { /* Cambia ampiezza */
    	if (clock^=1) {
    		up=600; low=150;
    	} else {
    		up=725; low=275;
    	}
    }
  }
}

lightsaber(a)
int a;
{
  d=damage()+a;
  while(dista(x,y) < (123450-4925*(damage()>d))) fire(dir);
}

main() /* Inizializza alcune variabili ed innesca la routine principale */
{ 
 if(radar(180*(loc_x()>500))>1)
 {
  setup(y=20+960*(posy=loc_y(x=20+960*(posx=loc_x()>500))>500));
 }
 else f2f();
 while (1)
 {
    if (radar3((dscan>>flag)&65535))
    {
      dir=(dvector>>flag)&65535;
      
      if (attack & (1<<flag))
      {
        if (damage(lightsaber(5))>d) attack&=~(1<<flag);/* else attack|=(1<<flag);*/
        timer+=2;
      }
      else
      {
        d=damage(drive(dir,100));
        while((speed()<100) || ((t<2) && (damage()==d))) spara();
        if(damage()>d)
        {
          lightsaber(0);
          ++timer;
        }
      }      
    }
    else
    {
      dir=(dang>>flag)&65535;
      lightsaber(5);
      timer+=2;
    }
    stop(sk=0); corner(flag^=16);
		if (++timer>tl)
		{
        i=enemies=0;
        while((i < 140)&&(enemies<2)) {
          enemies+=(scan(deg+i, 10)!=0);
          i+=20;
        }
        if (enemies<2) f2f=radar(deg1+45)<2;      
        if (!f2f)
        {
          if(!(td>>=1) && (damage()<60)) attack|=65537; else attack&=~65537;
          tl=timer+td;
        }
        else f2f();
    }
 }
}
