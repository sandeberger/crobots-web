degree(xx,yy) /* Direzione per x,y */
int xx,yy;
{
   return(dir=(360+((xx-=loc_x())<0)*180+atan(((yy-loc_y())*100000)/xx)));
}

dista(dx,dy) /* Distanza al quadrato (evita una sqrt())   */
int dx,dy;
{
        return (((dx-=loc_x())*dx+(dy-=loc_y())*dy));
}

corner() /* Si avvicina al corner */ 
{
	drive(degree(x,y), 100);
	while((dist=dista(x,y)) > 8100)
  {
   if(dist<25600) ;
   else fire(dir);  
  }
  stop(t=0);
}

int radar(sc)
{
int sc, en, dx;
  while((en<2) && (dx<160)) {
   en+=radar2(sc+dx); dx+=20;
  }
  return en;
}

int radar2(sc)
int sc;
{
  return (
   (scan(sc+10, 10)!=0) + (scan(-sc-10, 10)!=0)
  );
}

int radar3(sc)
int sc;
{
  return (
   scan(sc, 10) || scan(sc+20, 10)
  );
}

fire(dir)
{
  int asin,acos;
  
  if (speed()<100) drive(dir,100); else { if (scan(dir,10)) ang=dir; if (range>850) { ang+=120; } }
      
  if (scan(ang,10)) {  
    asin=(sin(ang-dir)/14384); 
    acos=(cos(ang-dir)/3796)-230;

    find();
    if (orange=scan(oang=ang,3)) {
      find();
      cannon(ang+(ang-oang)*((880+(range=scan(ang,10)))/482)-asin,
             range*230/(orange-range-acos)); 
    }  else search(); 
  } else search();  
}

find()
{
  if(scan(ang-13,10)) ang-=5;
  else if(scan(ang+13,10)) ang+=5;
  if(scan(ang+12,10)) ang+=4;
  else if(scan(ang-12,10)) ang-=4;
  if(scan(ang-11,10)) ang-=2;
  if(scan(ang+11,10)) ang+=2;
}

search()
{
  if (range=scan(ang+=350,10)) return cannon(ang,range);
  if (range=scan(ang+=20,10))  return cannon(ang,range);
  if (range=scan(ang+=320,10)) return cannon(ang,range);
  if (range=scan(ang+=60,10))  return cannon(ang,range);
  if (range=scan(ang+=280,10)) return cannon(ang,range);

  search(ang-=220);
}

f2f()
{
  up=700;
  low=300;
  while(1)
  {
    d=damage();
    while(loc_x() < up)  fire(dir=0);   stop();
    while(loc_y() > low) fire(dir=270); stop();
    while(loc_x() > low) fire(dir=180); stop();
    while(loc_y() < up)  fire(dir=90);  stop();
    				
    if ((damage()-d)>11) { /* Cambia ampiezza */
    	if (clock^=1) {
    		up=600; low=400;
    	} else {
    		up=700; low=300;
    	}
    }
  }
}

lightsaber(a)
int a;
{
  d=damage()+a;
  while(dista(x,y) < (123450-4925*(damage()>d))) fire(dir);
}

main() /* Inizializza alcune variabili ed innesca la routine principale */
{ 
 if(radar(180*(loc_x()>500))>1)
 {
  degree(x=20+960*(posx=loc_x()>500), y=20+960*(posy=loc_y()>500));
  corner(fp=3);
  dvector=(dang=(deg=dscan=deg1=(90*((posy<<1)+(posx^posy))))+15)+15;
  dvector|=((dvector+30)<<16);
  dang|=((dang+60)<<16);
  dscan|=((deg1+70)<<16);
  deg+=320;
  tl=td=38;
 }
 else f2f();
 while (1)
 {
    if (radar3((dscan>>flag)&65535))
    {
      dir=(dvector>>flag)&65535;
      
      if (attack & (1<<flag))
      {
        if (damage(lightsaber(5))>d) attack&=~(1<<flag);/* else attack|=(1<<flag);*/
        timer+=2;
      }
      else
      {
        d=damage();
        while((speed()<100) || ((++t<2) && (damage()==d))) fire(dir);
        if(damage()>d)
        {
          lightsaber(0);
          ++timer;
        }
      }      
    }
    else
    {
      dir=(dang>>flag)&65535;
      lightsaber(5);
      timer+=2;
    }
    stop(i=enemies=0); corner(flag^=16);
		if (++timer>tl)
		{
        while((i < 140)&&(enemies<2)) {
          enemies+=(scan(deg+i, 10)!=0);
          i+=20;
        }
        if (enemies<2) f2f=radar(deg1+45)<2;      
        if (!f2f)
        {
          if(!(td>>=1) && (damage()<60)) attack|=65537; else attack&=~65537;
          tl=timer+td;
        }
        else f2f();
    }
 }
