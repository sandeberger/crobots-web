	posx,     /* Variabili ad 1 bit                                   */
	posy,     /* salva posizione                                      */
	x,y,      /* Variabili salva posizione                            */
	dist,
	flag,
	timer,
	enemies,
	attack,
	f2f,
  t, sk, fp, i, td, tl,
  deg, up, low, clock, dang, deg1, dscan, dvector;
  

