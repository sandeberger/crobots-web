                              *** TATTICA ***

KakakaTz e' un robottino rompiscatole. Infatti prende possesso subito dell'
angolino piu' vicino all'inizio dell partita. Resta cosi' fermo aspettando che
qualcuno inizi ad ammazzarsi. Se qualche "pazzoide" cerca di avvicinarsi, Kak.
prontamente lo attacca, aventualmente andandogli incontro e rubando l'angolino
lasciato vuoto dallo sprovveduto attaccante.
Appena muore il primo sfortunello nell'arena, Kak. si guarda intorno e, se
vede ad un angolino adiacente ai suoi un robottino pressocche' statico, gli si
avvicina (ogni volta piu' precisamente), lo spara, e torna nel suo angolino.
Questa ultima tecnica non e' seguita da iLbEsTiO (il mio secondo robottino) che
si limita ad attendere con ansia in un angolino che qualcuno si avvicini o lo
spari per reagire e muoversi.
Se durante questo piccolo attacco, Kak. dovesse malauguratamente subire anche
il minimo danno, cambia subito obiettivo andando a cercarsi un altro robottino
statico. Nel caso in cui e' circondato da robottini vibranti o angolini vuoti 
senza nessuno da colpire , Kak. opta per trovarsi un altro angolino, magari
vicino a qualche robottino statico.
Nel momento in cui resta face-to-face con l'altro superstite nell'arena si
preoccupa subito di uscire in attacco sfruttando una piccola routine che gli
assicura di posizionarsi e camminare in modo tale da variare molto sia l'an-
golo che la distanza a cui e' visto dall'avversario; contemporaneamente cerca
di restare sempre abbastanza lontano dal suo nemico.
Se (purtroppo) dovesse arrivare a scontrarsi faccia a faccia con l'ultimo ri-
masto e di non avere sufficiente energia per affrontare lo scontro, Kak.
sceglie di scappare di angolino in angolino restando sempre a molta distanza
dal suo nemico.
Il comportamento, in difesa e in attacco e' molto simile al robottino iLbEsTiO
rispetto al quale, pero', attacca anche in presenza di due avversari rimasti
nell'arena.

                            *** PARTICOLARITA' ***

Alcune piccole innovazioni rispetto ai robottini "standard" visti fino al 1998:

1) quando Kak. va verso un angolino ci si attacca a massimo 7-8 metri di di-
   stanza.
2) le routine di fuoco soffrono del "bug zero" molto meno di quelle da cui de-
   rivano. Anche nel caso si debba puntare un robottino a 0 gradi precisi, Kak.
   lo vede e lo spara subito o lo rintraccia subito dopo!
3) la procedura di fuoco di goblin, derivata da tox, ha la miglioria che, nel
   caso ci si trovi a sparare sotto i 120 metri, lo si fa subito senza nessuna
   superflua correzione. Tutti i parametri sono stati poi corretti ed adattati
   al movimento del robottino.
4) la procedura d'attacco si basa sulla posizione del nemico e non su movimen-
   ti fissati o predeterminati.
5) KakakaTz e' uno dei miei due robottini e quindi e' sicuramente innovativo e
   fuori-standard!

